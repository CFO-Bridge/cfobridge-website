<script>
    // Add this script to your page
document.addEventListener('DOMContentLoaded', function() {
    // When the tool form is submitted
    document.querySelector('.tool-main-form').addEventListener('submit', function(e) {
        e.preventDefault();
        
        // Collect all answers
        let responses = {};
        for(let i = 1; i <= 16; i++) {
            const questionName = 'ques' + i;
            const selectedOption = document.querySelector('input[name="' + questionName + '"]:checked');
            if(selectedOption) {
                responses[questionName] = {
                    answer: selectedOption.value,
                    text: selectedOption.nextElementSibling.textContent.trim()
                };
            }
        }
        
        // Add the responses JSON to a hidden input
        const hiddenInput = document.createElement('input');
        hiddenInput.type = 'hidden';
        hiddenInput.name = 'responses';
        hiddenInput.value = JSON.stringify(responses);
        this.appendChild(hiddenInput);
        
        // Now submit the form
        this.submit();
    });
});
</script>